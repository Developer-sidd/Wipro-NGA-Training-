public class ArrayProgram {


    // Create an array of 5 elements of decimal type value and print the elements
    // one by one.
    public static void main(String[] args) {
        float arr[] = { 2.4f, 3.4f, 5.4f, 6.7f, 7.8f };
        System.out.println("The elements of array are -");
        for (int i = 0; i < 5; i++) {
            System.out.println(arr[i]);
        }

    }

}

